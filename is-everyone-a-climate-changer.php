                            <p><b>Introduction</b></p>
                            <p>Climate Change is a short term with a big impact. It refers to long-term shifts in temperatures and weather patterns. These shifts may be natural, but since the 1800s, human activities have been the main driver of climate change, primarily due to the burning of fossil fuels (like coal, oil, and gas), which produce heat-trapping gases like Carbon dioxide and methane. These gases are the cause of Global Warming. This further leads to the melting of glaciers which causes a rise in water level every year.
                            </p>
                            <p><b>Where do we step in…...</b></p>
                            <p>Now, one might ask if he/she is connected to this. The answer is a big YES. Everyone from rich to poor, short to tall, fat to slim is responsible for the climate change happening around us. They are unknowingly a reason for climate change whether it is positive or negative. Yes, climate change can be both negative and positive]. For those of you wondering what positive climate change is, they are the impacts of climate change which are beneficial for us.  </p>
