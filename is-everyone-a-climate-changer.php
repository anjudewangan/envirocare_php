                            <p><b>A mind-boggling FACT.</b></p>
                            <p>Let’s talk about an average person’s morning routine. He/she wakes up, brushes their teeth, takes a bath, eats breakfast and gets on any means of transport to get to their work.</p>
                            <p>Even in this short 3-4 hours of the time gap, the person has created an impact towards climate change. For example, when he/she brushes their teeth the amount of water they use is a key factor in CC because if they use a greater quantity of water, the purification company would have to use more of their factories to purify them.</p>
                            <p> Now, some of you might think that if they waste just 20-30 ml of water it is not going to make a difference but wait let me show you how cool maths is. Let’s say each person wastes only 1ml of water every day (which is less and not true at all) and only 1 billion out of the 8 billion people on this planet waste it.</p>
                            <p> So now each day 1,000,000,000 ml or 1 million litres of water is getting wasted. And again, this is not even the accurate results. So, you see how such a small thing created such a massive impact on climate. These small things when compiled make big impacts.
                            </p>
                            <p>Similarly, if we put this vice-versa then if a person only saves 1 ml of water each day and only 1 billion out of 8 billion people on this planet do so, we all will be able to save about 1 million litres of water every day. This was only the start of your day.</p>
                            <p><b>Conclusion</b></p>
                            <blockquote class="alime-blockquote">
                                <h4>Let’s take one more example. Let's say you travel to your workplace or school in your car or scooter. We all know that burning fuel releases greenhouse gases into the environment. If one person goes in one car and the second one goes in another car to the same place. That is 2x emission of harmful gases. But let’s say they both go in one car; it will reduce the emission by half. These are just some short instances from our daily life which lead us as a changing factor in the climate change journey. So by just thinking about it for some time, we can give our efforts to the planet and climate itself</h4>
