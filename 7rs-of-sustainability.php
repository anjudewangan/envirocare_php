                            <p><b>REFUSE-</b></p>
                            <p>Think before you buy and be prepared to not buy at all. Green consumers place themselves at the start of the buy-and-use cycle. They buy less and buy products that do the least harm to the environment.</p>
                            <p><b>REDUCE-</b></p>

                            <p>“Reduce” could be seen as the most important R because all 7 Rs seek to reduce the amount of waste that we’re putting into the natural environment. As such, “reduce” can be seen as a guiding principle for all our climate action. In concrete terms though, it’s not just about reducing waste. We should also reduce the things we buy and use in the first place</p>

                            <p><b>REUSE-</b></p>

                            <p>Reusing items not only helps to reduce unnecessary waste but also helps us to value products and materials and release their full potential. “Reuse,” asks us to question behaviors that are often unconscious, for example throwing away takeaway containers. Instead, we should consider whether we can reuse them.</p>
                            <p><b>REPAIR-</b></p>

                            <p>“Repair” is an important piece of the sustainability puzzle. In today’s throwaway culture, it’s all too common to get rid of items that could easily be repaired. We live in a society where everything’s treated as expendable, and material items aren’t valued as they once were. Fostering a culture of repairing things instead of throwing them away could vastly reduce the amount of waste that we collectively produce.</p>
                            <p><b>REGIFT-</b></p>
                            <p>When you regift, you give someone a gift that you received from someone else. Don't feel guilty! Passing it on to someone who will enjoy it more than you is a good thing. You just need to follow this etiquette: You are certain the gift is something the recipient would enjoy. The gift is brand new and comes in its original box. The gift isn't handmade or a family treasure.</p>
                            <p><b>RECYCLE-</b></p>

                            <p>The final “R” is perhaps the most well-known. Recycling is often touted as an eco-friendly solution, and while it is better than waste going to landfills, it should be a last resort. Often, it’s only possible to recycle a certain percentage of the original product, so some of it still gets wasted. A lot of products end up being contaminated in the recycling bin so can’t be recycled anyway, and many materials can only be recycled a finite number of times. That’s not to say we shouldn’t recycle, just that it’s only one piece of the puzzle. We should reduce the number of new items we produce and throw them out in the first place.</p>
                            <blockquote class="alime-blockquote">
                                <h4>Many of the 7Rs might seem quite small when we think about them in relation to one item. However, the more of us who follow them, the more others will learn about them and be inspired to do the same.</h4>
