                            <p><b>Introduction</b></p>
                            <p>We all are consumers. What we purchase and how we dispose of waste can harm the environment. A big of saving the environment is choosing the kind of consumer we are going to be. People concerned about the environment think about the environmental impact of what they purchase and whether they need to buy at all.</p>
                            <p>7Rs. Rethink, Refuse, Reduce, Reuse, Repair, Regift, and Recycle! All these 7 concepts are focused on minimizing waste and taking steps towards sustainability work your way through all of them and you will be well on your way to saving the environment and living a zero-waste life.</p>
                            <p><b>A brief explanation of each of the 7RS</b></p>
                            <p><b>RETHINK-</b></p>
                            <p>Every time, you go shopping to buy new clothes, or gadgets, think do you need them? Consider going for a second-hand product. This way you will save resources and things from the trash pile. Instead of buying things from a popular international brand, go for local products.</p>
                            <p>This way, you will help to shorten the supply chain, which decreases transportation energy, and use of packaging, and at the same time you support small entrepreneurs.</p>
