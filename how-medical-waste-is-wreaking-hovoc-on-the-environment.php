                            <p><b>Introduction</b></p>
                            <p>Have you ever stopped to think about the impact of medical waste on the environment? The World Health Organization defines medical waste as "waste generated by healthcare activities," encompassing a wide range of materials, from chemicals to needles and syringes. But what are the consequences of this waste for our planet?</p>
                            <p> Healthcare activities generate a significant amount of waste, with 15% of all medical waste being hazardous and infectious. Not only does this waste contain potentially harmful microorganisms that can infect communities, but it also contributes to environmental pollution, exacerbating the effects of air pollution. A study by Wei et al. (2021) predicts that medical waste in China will reach a staggering 2.7 × 109 kg per year by 2030. Does this mean that the environmental effects will increase in direct proportion to the rise in medical waste? The answer is yes.</p>

