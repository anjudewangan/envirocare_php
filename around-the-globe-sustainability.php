                            <p><b>Things happening around regarding sustainability? </b></p>
                            <p>As populations around the world continue to increase and the climate crisis grows more concerning with each passing day, the need for sustainable development in all shapes and sizes has never been higher. This means we need new construction methods, materials, and strategies that are as eco-friendly as possible. There is so much happening all around regarding sustainability such as summits, webinars, workshops, etc. </p>
                            <p>Countries around the globe have been making positive changes to protect the environment as Costa Rica is on their way to becoming the first carbon-neutral country, The island of Sanso is already running on 100% renewable energy, including solar, wind, and biomass similarly in Switzerland all waste is properly disposed of and the rest is turned into energy.</p>
                            <p><b>Sustainable initiatives. </b></p>
                            <p>The Sustainable Development Goals (SDGs), also known as the Global Goals, were adopted by the United Nations in 2015 as a universal call to action to end poverty, protect the planet, and ensure that by 2030 all people enjoy peace and prosperity. The 17 SDGs are integrated—they recognize that action in one area will affect outcomes in others and that development must balance social, economic, and environmental sustainability.</p>
                            <p>The year 2022 has not been that bad for the sustainable world in fact we have had quite a lot of wins starting with the UN Plastics Treaty, Brazil’s regime change, we got a new solar landmark in India, and many more.</p>
                            <p><b>Conclusion</b></p>
                            <blockquote class="alime-blockquote">
                                <h4>Summing it up, the economic development that we have achieved so far has adopted various techniques that proved to be harmful to the environment. The compelling need is for sustainable development which aims at the development of society without having a negative impact on the environment </h4>
