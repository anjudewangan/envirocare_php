                            <p><b>What is sustainability? Why has it suddenly become such a hot topic?</b></p>
                            <p>Sustainability is commonly described as having three pillars; environmental, economic, and social. It is focused on countering major environmental problems such as climate change, deforestation, ocean acidification, etc. We live in a world with finite resources the more we can reduce waste and be more efficient the longer those resources will last. Sustainability is becoming a mainstream topic across the board now.</p>
                            <p>Many activists such as Greta Thunberg have come together to save the planet and to stop us from destroying it further and using the resources more meticulously.</p>
