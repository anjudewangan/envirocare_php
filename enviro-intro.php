                <p class="sample-text">
                    <b>Climate change:</b> it continues to plague
                    humanity in ways more than just one.
                    We, as communities, can feel its
                    negative impacts on our health and
                    wellbeing, productivity, and other areas
                    of our life. Yet, only so many of us
                    transition to a more <u>“sustainable”</u>
                    lifestyle: the buzzword these days. To
                    tackle this issue, we brainstormed and
                    thought to ourselves,<i> “how can we help
                        others adopt a healthier, more <b>“climate-
                            friendly”</b> lifestyle?”</i> and Envirocare
                    Foundation was then conceived.
                </p>
                <p>As an organization, we endeavor to
                    work towards supporting our community
                    in becoming change-makers with values
                    of global citizenship. Through
                    advocacy-oriented work, we aim to
                    emphasize the significance of
                    sustainability in our daily actions, and
                    how small actions, when collective, can
                    lead to <i>big changes, environmentally</i>.
                    We hope to instill a sense of
                    appreciation for sustainable living, in
                    our community.</p>
                <p><b>What distinguishes us from the
                        countless other sustainability initiatives?</b>
                    We are four committed and motivated
                    teenagers striving to catalyze our
                    journey to a sustainable future. We
                    work towards changing behaviors and
                    attitudes towards the environment. To
                    this end, we reinforce the importance of
                    our relationship with the natural world.
                    By using psychological concepts of
                    behavioral change, we aim to help
                    individuals adopt long-term habits.</p>
                <p><i>“One of the greatest threats to our
                        planet is the belief that someone else
                        will save it,”</i> has been rightly stated.
                    How will the <b>environment change</b> if we
                    ourselves don’t change? This quote is a
                    testimony to the significance of a
                    <u>climate-friendly</u> lifestyle.
                    So, join us today, as we hope to nurture
                    healthier and happier communities, with
                    compassion towards the natural world.
                </p>
